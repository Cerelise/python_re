
# 加减乘除
apples = 3
apples = apples + 1
# apple += 1
print(f"+1 : {apples}") # 4

# apples = apples - 1
apples -= 1
print(f"-1 : {apples}") # 3

apples = apples * 2
# apples *= 2
print(apples)   # 6

apples = apples / 3
apples /= 2
print(apples) # 1.0 => float

# 指数 平方、三次方
bananas = 3
# bananas = bananas ** 3
bananas **= 3
print(bananas)


# 模数 mod
# 10 mod 3 = 3 余 1
print(10 % 3)
# 11 mod 3 = 3 余 2
print(11 % 3)
# 12 mod 3 = 4 余 0
print(12 % 3)





